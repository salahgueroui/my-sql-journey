-- =============================================
-- Views Use Cases
-- =============================================

-- #1 Use Case: Central Logic
--    > Centralize reusable query logic in one place so all consumers get consistent results.

-- #2 Use Case: Hide Complexity
--    > Simplify complex joins and subqueries behind a clean, easy-to-use view.

-- #3 Use Case: Data Security
--    > Restrict access to sensitive columns/rows by exposing only what users need.

-- #4 Use Case: Flexibility & Dynamic
--    > Provide a flexible layer that can adapt to changing requirements without altering base tables.

-- #5 Use Case: Multiple Languages
--    > Serve content in different languages through views tailored to each locale.

-- #6 Use Case: Virtual Marts
--    > Create virtual data marts for reporting without duplicating physical data.
